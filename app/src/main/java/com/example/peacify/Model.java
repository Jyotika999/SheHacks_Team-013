package com.example.peacify;

public class Model {
    String description;
    String time;

    public Model(){

    }

    public void setDescription(String description) {
        this.description = description;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDesc() {
        return description;
    }
    public String getTime() {
        return time;
    }
}
